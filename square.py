
def area(a):
    '''
        Принимает число a, возвращает площадь квадрата с такой стороной
        При вызове функции со значением 4 вывод: 16
    '''
    return a * a

print(area(4))

def perimeter(a):
    '''
        Принимает число a, возвращает периметр квадрата с такой стороной
        При вызове функции со значением 3 вывод: 12
    '''
    return 4 * a

print(perimeter(3))
