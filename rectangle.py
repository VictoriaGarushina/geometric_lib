
def area(a, b):
    '''
        Принимает число a и b, возвращает площадь прямоугольника с такими сторонами
        При вызове функции со значениями a = 4, b = 3 вывод: 12
    '''
    return a * b

print(area(4,3))

def perimeter(a,b):
    '''
        Принимает число a и b, возвращает периметр прямоугольника с такими сторонами
        При вызове функции со значениями a = 3, b = 2 вывод: 10
    '''
    return (a + b) * 2

print(perimeter(3,2))
